export function getThemeByName(name: string): ITheme {
  const preset = themePresets.find((t) => t.name === name);
  return preset ? preset.theme : terminalTheme;
}

export function buildCustomTheme(
  baseName: string,
  overrides: { background?: string; foreground?: string; cursor?: string }
): ITheme {
  const base = getThemeByName(baseName);
  return {
    ...base,
    ...(overrides.background ? { background: overrides.background } : {}),
    ...(overrides.foreground ? { foreground: overrides.foreground } : {}),
    ...(overrides.cursor ? { cursor: overrides.cursor } : {}),
  };
}
